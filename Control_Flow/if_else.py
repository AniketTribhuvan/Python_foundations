
# if-else are the conditional statements used for decision making.

age = int(input("Enter your age = "))

if(age >= 18):
    print("You're eligible to vote.")       # The space is indent which represents block
    print("Please vote")
else:
    print("You aren't eligible to vote.")
print("Hi")                                 


# In above program print("Hi") doesn't consist indent so it is not a part of else, it's independent so it will be printed everytime.


# space before print represents indentation. Unlike C instead of using brackets, 
# python uses indentation.
# indentation is like a block of code starts from space.