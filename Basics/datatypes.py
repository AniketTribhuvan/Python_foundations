# Python Data Types

name = "Aniket"      # str -> stores text
age = 16             # int -> whole number
height = 5.8         # float -> decimal number
is_learning = True   # bool -> True or False

# Printing values
print(name)
print(age)
print(height)
print(is_learning)

# Checking data types
print(type(name))
print(type(age))
print(type(height))
print(type(is_learning))

# Output
# Aniket
# 16
# 5.8
# True
# <class 'str'>
# <class 'int'>
# <class 'float'>
# <class 'bool'>