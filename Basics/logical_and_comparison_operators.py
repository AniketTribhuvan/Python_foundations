age = 16
marks = 85

# Comparison operators
print(age > 18)      # False
print(age == 16)     # True
print(marks != 90)   # True

# Logical operators
print(age > 15 and marks > 80)   # True
print(age > 18 or marks > 80)    # True
print(not(age > 18))             # True

# Output
# False
# True
# True
# True
# True
# True